def out2b():
    # Создаем словарь для хранения количества студентов по предметам
    subject_counts = {}

    # Проходим по каждому студенту и каждому предмету в его зачетке
    for student in students:
        for subject in student["Зачетка"]:
            # Если предмет еще не встречался, добавляем его в словарь
            if subject["Предмет"] not in subject_counts:
                subject_counts[subject["Предмет"]] = 0
            # Увеличиваем счетчик студентов для данного предмета
            subject_counts[subject["Предмет"]] += 1

    # Выводим результаты в виде таблицы
    print("{:<20}{}".format("Предмет", "Количество студентов"))
    for subject, count in subject_counts.items():
        print("{:<20}{}".format(subject, count))

